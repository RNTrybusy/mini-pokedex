class Pokemon {
    // Propriedades (atributos) que representam as informações de um Pokémon
        name; // Nome do Pokémon
        japaneseName; // Nome do Pokémon em japonês
        number; // Número na Pokédex
        type; // Tipo principal do Pokémon
        types = []; // Lista de todos os tipos do Pokémon
        photo; // URL ou caminho para uma foto/imagem do Pokémon
        height; // Altura do Pokémon (normalmente em metros)
        weight; // Peso do Pokémon (normalmente em quilogramas)
        baseExperience; // Experiência base do Pokémon (valor usado em cálculos de XP nos jogos)
    }